/**
 * Type Conversion
 */

// String to Number Conversion

let amount = "100";
amount = parseInt(amount); // Using parseInt() method
amount = Number(amount); // Using Number() method

// Number to String Conversion
let num1 = 31;
num1 = num1.toString(); // Using toString() method
num1 = String(num1); // Using Number() method

// Decimals Conversion using parseFloat() method
let num2 = 5.5;
num2 = parseFloat(num2);

// Boolean Conversions
let x = true;
x = Number(x); // From boolean to number

let y = 1;
y = Boolean(y); // From number to boolean

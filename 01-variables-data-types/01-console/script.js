// Log a number
console.log(123);

// Log a string
console.log("Hello World!");

// Log multiple values
console.log(123, "Hello", true);

// Log a variable
let x = 100;
console.log(x);

// Using console.error() method
console.error("This is an error message!");

// Using console.warn() method
console.warn("This is a warning!");

// Using console.table() method
console.table({ name: "Damjan", username: "Damjan15" });

// Log with styles
const styles = "padding: 10px; background-color: black; color: green;";
console.log("%cHello", styles);